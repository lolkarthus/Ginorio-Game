function newSkill(name, magnitude, effect, hitChance, coolDown, lvlReq, strReq, dexReq, klass) {
	this.name = name
	this.magnitude = magnitude
	this.effect = effect
	this.hitChance = hitChance
	this.coolDown = coolDown
	this.lvlReq = lvlReq 
	this.strReq = strReq
	this.dexReq = dexReq
	this.class = klass
}